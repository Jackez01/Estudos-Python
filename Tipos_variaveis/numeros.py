#inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

#real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

#type()
print("Tipo da variavel inteiro = ", type(numero_inteiro))

#soma
soma = 1 + 1
print("1+1 = ", soma)

#subtração
sub = 2 - 1
print("2-1 = ", sub)

#mult *
mult = 2 * 2
print("2 x 2 = ", mult)

#div
div = 5 / 3
print("5 / 3 = ", div)
