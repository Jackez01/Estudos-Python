#declaração 

minha_lista = [1, 2, 3, 4, 5]

print("Minha lista de exemplo:", minha_lista)
print(minha_lista[0])

#para acessar uma parte da lista [1(primeiro elemento a ser pegado): 3(vai pegar ate o elemento 2, sempre adicionar mais um)]
print(minha_lista[1:3])

#para ficar melhor pode fazer dessa forma: (sempre adicionando um a mais) vai pegar do inicio até o alvo
print(minha_lista[:3])

#para pegar de um elemento para frente
print(minha_lista[1:])